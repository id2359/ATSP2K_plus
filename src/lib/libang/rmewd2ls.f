      SUBROUTINE RMEWD2LS(J,L,COEF)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION ID111L(136),ID030L(136),ID131L(136)
      DATA ID111L/3*0,-2700,-420,600,0,480,3360,240,2*0,4800,1050,
     :3375,3*0,2430,-2625,-75,2*0,-2100,240,0,-2400,3*0,-1680,30,0,
     :300,6*0,-4200,3000,2400,120,8400,4*0,4200,120,-960,-1920,840,
     :-210,5*0,-1200,2*0,2100,-525,9*0,-5400,-1350,-1215,1458,8*0,
     :-1800,4500,0,-540,9*0,-1980,-495,0,-1485,13*0,4752,3960,1440,
     :1188,14*0,-3510,0/
      DATA ID030L/14*0,-53900,3*0,39600,0,5940,6*0,28160,7*0,-880,
     :2*0,-77616,5*0,38808,3*0,-693,0,34650,3*0,-43659,0,-12320,
     :5*0,-28875,2*0,1617,3*0,40095,0,-990,3*0,-38115,0,16335,6*0,
     :-39600,5*0,-43560,7*0,-27225,2*0,-3465,2*0,23805,5*0,43560,
     :3*0,38115,0,70785,13*0,27027,2*0,28665,0,20384/
      DATA ID131L/12*0,58800,-16800,110250,2*0,-42000,-7680,15750,
     :-36450,2*0,33600,29040,0,14400,3*0,-1680,-5880,0,28800,2*0,
     :-88200,2520,117600,3360,235200,0,8400,15120,-58800,0,10080,
     :11760,-35490,14700,15120,-41160,-945,94080,1470,2*0,-29400,
     :-13125,0,9450,3*0,33075,3*0,75600,-1350,94500,-10800,-48600,
     :16875,0,103950,10395,16038,2*0,-25200,18000,0,36000,2*0,
     :-138600,2*0,95040,3*0,-27720,24255,0,-24750,2*0,20160,25515,
     :0,-135,6*0,-115500,-29700,-26400,0,184800,-11550,10395,7722,
     :-102960,28665,-168168,8*0,109200,-6825,0,-85995,2*0,76440,0/
      IF(L.EQ.0) THEN
        ID=ID111L(J)
        IV=25
      ELSEIF(L.EQ.1) THEN
        ID=ID030L(J)
        IV=770
      ELSEIF(L.EQ.2) THEN
        ID=ID131L(J)
        IV=350
      ENDIF
      IF(ID.GE.0) THEN
        COEF=DSQRT(DBLE(ID))
      ELSE
        COEF=-DSQRT(-DBLE(ID))
      ENDIF
      COEF=COEF/DSQRT(DBLE(IV))
      RETURN
      END
